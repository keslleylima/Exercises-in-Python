number = input('type a number\n')

if len(number) == 1:
    print('unity: ', number[0])

if len(number) == 2:
    print('unity: ', number[0])
    print('ten: ', number[1])

if len(number) == 3:
    print('unity: ', number[0])
    print('ten: ', number[1])
    print('hundred: ', number[2])

if len(number) == 4:
    print('unity: ', number[0])
    print('ten: ', number[1])
    print('hundred: ', number[2])
    print('thousands: ', number[3])

